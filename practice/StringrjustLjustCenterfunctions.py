print("hello world".rjust(15)) #it inscreases the string length to 15 by adding space at the right side
#'    hello world'
print("hello world".ljust(15)) #it inscreases the string length to 15 by adding space at the left side
# 'hello world    '
print("hello world".ljust(15,"-")) #it inscreases the string length to 15 by adding '-'' at the left side
# hello world----
print("hello world".rjust(15,"*")) #it inscreases the string length to 15 by adding '*' at the left side
# ****hello world
print("hello world".center(15)) #it inscreases the string length to 15 by adding space and moved string to center
# '  hello world  '

